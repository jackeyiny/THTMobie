  function KTDN() {
    let mauKT = /^[A-Za-z0-9]{6,16}$/;
    if ($("#txtDN").val() == "") {
      $("#tbDN").html("Không để trống");
      return false;
    }
    if (!mauKT.test($("#txtDN").val())) {
      $("#tbDN").html("* Chỉ được chữ và số, từ 6-16 kí tự");
      return false;
    }
    $("#tbName").html("*");
    return true;
  }
